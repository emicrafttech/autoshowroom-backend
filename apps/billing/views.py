def _format_ngn(amount: int) -> str:
    return f"NGN {int(amount):,}"


def _invoice_pdf(invoice: Invoice) -> BytesIO:
    buffer = BytesIO()
    pdf = canvas.Canvas(buffer, pagesize=letter)
    width, height = letter
    dealer_name = invoice.dealer.name if invoice.dealer_id else "Dealer"
    plan_name = invoice.subscription.plan.name if invoice.subscription_id and invoice.subscription else "Subscription"

    pdf.setTitle(f"Invoice {invoice.id}")
    pdf.setFont("Helvetica-Bold", 20)
    pdf.drawString(72, height - 72, "AutoShowroom Invoice")
    pdf.setFont("Helvetica", 10)
    pdf.drawRightString(width - 72, height - 72, f"Invoice ID: {invoice.id}")
    pdf.drawRightString(width - 72, height - 88, f"Issued: {invoice.issued_at:%Y-%m-%d}")

    pdf.setFont("Helvetica-Bold", 12)
    pdf.drawString(72, height - 130, "Bill To")
    pdf.setFont("Helvetica", 11)
    pdf.drawString(72, height - 148, dealer_name)

    pdf.setFont("Helvetica-Bold", 12)
    pdf.drawString(72, height - 210, "Description")
    pdf.drawRightString(width - 72, height - 210, "Amount")
    pdf.line(72, height - 220, width - 72, height - 220)

    tax = vat_breakdown(invoice.amount_ngn)
    amount_ex_vat = invoice.amount_ex_vat_ngn or tax["amountExVatNgn"]
    vat_amount = invoice.vat_ngn or tax["vatNgn"]

    pdf.setFont("Helvetica", 11)
    pdf.drawString(72, height - 245, plan_name)
    pdf.drawRightString(width - 72, height - 245, _format_ngn(invoice.amount_ngn))
    pdf.drawString(72, height - 265, "Amount excluding VAT")
    pdf.drawRightString(width - 72, height - 265, _format_ngn(amount_ex_vat))
    pdf.drawString(72, height - 285, "VAT (7.5%)")
    pdf.drawRightString(width - 72, height - 285, _format_ngn(vat_amount))

    pdf.line(72, height - 305, width - 72, height - 305)
    pdf.setFont("Helvetica-Bold", 12)
    pdf.drawString(72, height - 330, f"Status: {invoice.get_status_display()}")
    pdf.drawRightString(width - 72, height - 330, f"Total (VAT incl.): {_format_ngn(invoice.amount_ngn)}")

    pdf.setFont("Helvetica", 9)
    pdf.drawString(72, 72, "Generated by AutoShowroom. Prices include VAT at 7.5%.")
    pdf.showPage()
    pdf.save()
    buffer.seek(0)
    return buffer


