class InvoicePdfView(EnvelopeMixin, APIView):
    permission_classes = [IsActiveDealerStaff]

    def get(self, request, invoice_id):
        queryset = Invoice.objects.select_related("dealer", "subscription__plan")
        if not has_vehicle_review_permission(request.user):
            queryset = queryset.filter(dealer_id=request.user.dealer_id)
        invoice = get_object_or_404(queryset, id=invoice_id)
        filename = f"autoshowroom-invoice-{invoice.issued_at:%Y%m%d}-{str(invoice.id)[:8]}.pdf"
        return FileResponse(
            _invoice_pdf(invoice),
            as_attachment=True,
            filename=filename,
            content_type="application/pdf",
        )


class CheckoutView(EnvelopeMixin, APIView):
    permission_classes = [IsActiveDealerStaff]

    def post(self, request):
        serializer = CheckoutSerializer(data=request.data)
        serializer.is_valid(raise_exception=True)
        dealer = request.user.dealer
        plan = get_object_or_404(BillingPlan, id=serializer.validated_data["planId"], is_active=True)
        billing_interval = serializer.validated_data.get("billingInterval") or "monthly"
        active = get_active_subscription(dealer)

        if (
            active
            and dealer.plan_id == plan.id
            and active.billing_interval == billing_interval
            and active.status != Subscription.Status.TRIALING
        ):
            raise ValidationError("You are already on this plan.")

        quote = compute_checkout_quote(dealer, plan, billing_interval=billing_interval)
        reference = build_checkout_reference(dealer.id, plan.id)
        callback_url = f"{billing_callback_url()}?checkout={reference}"
        amount_kobo = quote["amount_kobo"]
        amount_ngn = quote["amount_ngn"]
        tax = vat_breakdown(quote["list_price_ngn"])

        init_payload = {
            "dealerId": str(dealer.id),
            "planId": plan.id,
            "amountNgn": amount_ngn,
            "amountKobo": amount_kobo,
            "listPriceNgn": quote["list_price_ngn"],
            "creditAppliedNgn": quote["credit_applied_ngn"],
            "checkoutKind": quote["checkout_kind"],
            "billingInterval": billing_interval,
            "foundingTrial": quote.get("founding_trial", False),
            "trialDays": quote.get("trial_days", 0),
            "preservePeriodEnd": (
                quote["preserve_period_end"].isoformat()
                if quote["preserve_period_end"]
                else None
            ),
        }

        if amount_ngn <= 0:
            PaymentEvent.objects.create(
                event_type="checkout.initiated",
                reference=reference,
                payload={**init_payload, "fullyCovered": True},
            )
            return Response(
                {
                    "planId": plan.id,
                    "reference": reference,
                    "fullyCovered": True,
                    "publicKey": public_key(),
                    "amountNgn": amount_ngn,
                    "listPriceNgn": quote["list_price_ngn"],
                    "creditAppliedNgn": quote["credit_applied_ngn"],
                    "checkoutKind": quote["checkout_kind"],
                    "billingInterval": billing_interval,
                    "foundingTrial": quote.get("founding_trial", False),
                    "trialDays": quote.get("trial_days", 0),
                    "vatNgn": tax["vatNgn"],
                    "amountExVatNgn": tax["amountExVatNgn"],
                },
                status=status.HTTP_201_CREATED,
            )

        if not is_configured():
            raise ValidationError("Paystack is not configured. Contact support.")

        PaymentEvent.objects.create(
            event_type="checkout.initiated",
            reference=reference,
            payload=init_payload,
        )

        return Response(
            {
                "planId": plan.id,
                "reference": reference,
                "fullyCovered": False,
                "publicKey": public_key(),
                "email": request.user.email,
                "amountNgn": amount_ngn,
                "amountKobo": amount_kobo,
                "listPriceNgn": quote["list_price_ngn"],
                "creditAppliedNgn": quote["credit_applied_ngn"],
                "checkoutKind": quote["checkout_kind"],
                "billingInterval": billing_interval,
                "foundingTrial": quote.get("founding_trial", False),
                "trialDays": quote.get("trial_days", 0),
                "vatNgn": tax["vatNgn"],
                "amountExVatNgn": tax["amountExVatNgn"],
                "currency": payment_currency(),
                "callbackUrl": callback_url,
                "metadata": {
                    "dealer_id": str(dealer.id),
                    "plan_id": plan.id,
                    "expected_amount_kobo": amount_kobo,
                    "checkout_kind": quote["checkout_kind"],
                    "billing_interval": billing_interval,
                },
            },
            status=status.HTTP_201_CREATED,
        )


